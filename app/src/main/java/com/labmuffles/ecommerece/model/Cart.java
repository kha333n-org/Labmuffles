package com.labmuffles.ecommerece.model;

public class Cart {

    private String pid, name, price, quantity, time, discount, description, date;

    public Cart() {
    }

    public Cart(String pid, String name, String price, String quantity, String time, String discount, String description, String date) {
        this.pid = pid;
        this.name = name;
        this.price = price;
        this.quantity = quantity;
        this.time = time;
        this.discount = discount;
        this.description = description;
        this.date = date;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDiscount() {
        return discount;
    }

    public void setDiscount(String discount) {
        this.discount = discount;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
